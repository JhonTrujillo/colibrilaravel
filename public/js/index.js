/*Funcion de boton mostrar/ocultar barra de navegacion index.html*/

function mostrar(){
    document.getElementById('barranav').style.display = 'block';
    document.getElementById('btmostrar').style.display = 'none';
    document.getElementById('btocultar').style.display = 'block';
}
function ocultar(){
    document.getElementById('barranav').style.display = 'none';
    document.getElementById('btmostrar').style.display = 'block';
    document.getElementById('btocultar').style.display = 'none';
    
}


